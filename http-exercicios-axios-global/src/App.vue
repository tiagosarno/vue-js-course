<template>
	<div id="app" class="container">
		<h1>HTTP com Axios</h1>
		<b-alert show dismissible 
			v-for="mensagem in mensagens" 
			:key="mensagem.texto"
			:variant="mensagem.tipo">{{ mensagem.texto}}</b-alert>
		<b-card>
			<b-form-group label="Nome:">
				<b-form-input type="text" 
					placeholder="Informe o nome"
					size="lg"
					v-model="usuario.nome"></b-form-input>				
			</b-form-group>
			<b-form-group label="E-mail:">
				<b-form-input type="email" 
					placeholder="Informe o e-mail"
					size="lg"
					v-model="usuario.email"></b-form-input>
			</b-form-group>
			<hr />
			<b-button @click="salvar" class="mr-2"
				size="lg"
				variant="primary"
				>Salvar</b-button>

			<b-button @click="listar"
				size="lg"
				variant="secondary"
				>Listar Usuários</b-button>

			<hr />

			<b-list-group>
				<b-list-group-item 
					v-for="(usuario, id) in usuarios" 
					:key="id">
					<strong>ID: </strong> {{ id }}<br />
					<strong>Nome: </strong> {{ usuario.nome }}<br />
					<strong>E-mail: </strong> {{ usuario.email }}<br />
					<b-button 
						variant="warning"
						size="lg"
						@click="carregar(id)">Carregar</b-button>
					<b-button 
						variant="danger"
						size="lg"
						class="ml-2"
						@click="excluir(id)">Excluir</b-button>
				</b-list-group-item>
			</b-list-group>
		</b-card>
	</div>
</template>

<script>
export default {
	data(){
		return{
			mensagens: [],
			id: null,
			usuarios: [],
			usuario: {
				nome: '',
				email: ''
			}
		}
	},
	methods: {
		limpar(){
			this.usuario.nome = ''
			this.usuario.email = ''
			this.id = null,
			this.mensagens = []
		},
		salvar(){
			const metodo = this.id ? 'patch' : 'post'
			const finalUrl = this.id ? `/${this.id}.json` : '.json'
			this.$http[metodo](`/usuarios${finalUrl}`, this.usuario)
				.then(() => {
					this.limpar()
					this.mensagens.push({
						texto: 'Operação realizada com sucesso!',
						tipo: 'success'
					})
				})			
		},
		listar(){
			this.$http.get('usuarios.json').then(res => {
				this.usuarios = res.data
			})

			// Exemplo:
			// Este tipo de chamada pode ser utilizado após o login válido
			// para se obter um token de acesso...
			this.$http.defaults.headers.common['Authorization'] = 'abc123'
		},
		carregar(id){
			this.id = id
			this.usuario = { ...this.usuarios[id] }
		},
		excluir(id){
			this.$http.delete(`/usuarios/${id}.json`)
				.then(() => {
					this.limpar()
					this.mensagens.push({
						texto: 'Operação realizada com sucesso!',
						tipo: 'success'
					})
				})
				.catch(() => {
					this.mensagens.push({
						texto: 'Problema para Excluir!',
						tipo: 'danger'
					})
				})
		}
	}
	// created() {
